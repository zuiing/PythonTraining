from turtle import *
def nose(x,y):#鼻子
    color((255, 155, 192), "pink")
    penup()
    goto(x,y)
    pendown()
    setheading(-30)
    begin_fill()
    a=0.4
    for i in range(120):
        if 0<=i<30 or 60<=i<90:
            a=a+0.06
            left(3) #向左转3度
            forward(a) #向前走a的步长
        else:
            a=a-0.06
            left(3)
            forward(a)
    end_fill()#填充完成
    penup()
    setheading(90)
    forward(18)
    setheading(0)
    backward(4)
    setheading(0)
    forward(10)
    pendown()
    pencolor(255,155,192)#画笔颜色
    setheading(10)
    begin_fill()
    circle(5)
    color(160,82,45)#返回或设置pencolor和fillcolor
    end_fill()

    penup()
    setheading(0)
    forward(18)
    pendown()
    pencolor(255, 155, 192)
    setheading(10)
    begin_fill()
    circle(5)
    color(160, 82, 45)
    end_fill()
